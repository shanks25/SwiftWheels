<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Illuminate\Http\Request;
use Illuminate\Contracts\Queue\ShouldQueue;

class mailtrap extends Mailable
{
    use Queueable, SerializesModels;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build(Request $request)
    {
       return $this->view('email',['name'=>$request->name,'msg'=>$request->msg,'mobile'=>$request->mobile,'email'=>$request->email,'address'=>$request->address])->to('info@swift-wheels.com')->subject('Website Contact Form');
    }
}
